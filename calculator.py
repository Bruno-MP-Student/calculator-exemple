def add(a: int, b: int) -> int:
    """
    Adds two numbers.

    :param a: First number
    :param b: Second number
    :return: Sum of a and b
    """
    return a + b

def subtract(a: int, b: int) -> int:
    """
    Subtracts the second number from the first.

    :param a: First number
    :param b: Second number
    :return: Difference between a and b
    """
    return a - b
